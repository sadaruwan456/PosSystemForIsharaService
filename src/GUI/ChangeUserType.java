    public void reset() {
        nic.setText(null);
        password.setText(null);
        usertype.setSelectedIndex(0);
        username.setText("User");

    }

    private void loadType() {
        try {
            String qry = "SELECT type FROM user_type";
            ResultSet rs = DB.search(qry);
            while (rs.next()) {
                usertype.addItem(rs.getString("type"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
