    private void nicFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nicFocusLost

        // TODO add your handling code here:
        if (!(nic.getText().isEmpty())) {
            if (!(Validation.validateNic(nic.getText()))) {
                JOptionPane.showMessageDialog(this, "NIC you entered is not a valid NIC", "Invalid data", JOptionPane.ERROR_MESSAGE);
                nic.grabFocus();
                nic.setText(null);

            }
        }

    }//GEN-LAST:event_nicFocusLost

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean value = true;
        String NIC = nic.getText();
        String PasswordToAccept = password.getText();
        String UserType = usertype.getSelectedItem().toString();
        if (NIC.isEmpty()) {
            JOptionPane.showMessageDialog(this, "NIC cannot be empty", "Empty data", JOptionPane.ERROR_MESSAGE);
              nic.grabFocus();
              value = false;
        }

        if (!(Validation.validateNic(nic.getText()))) {

            JOptionPane.showMessageDialog(this, "Incorrect NIC", "Error", JOptionPane.ERROR_MESSAGE);
            value = false;
            nic.grabFocus();

        }
        if(value){
            try {
                ResultSet rs = DB.search("SELECT password FROM user WHERE employee_nic = '"+NIC+"'");
                if(rs.next()){
                    if(rs.getString("password").equals(Cryption.encript(PasswordToAccept))){
                       rs = DB.search("SELECT id FROM user_type WHERE type = '"+UserType+"' ");
                       if(rs.next()){
                        DB.iud("UPDATE user SET user_type_id='"+rs.getString("id")+"' WHERE employee_nic = '"+NIC+"'"); 
                        JOptionPane.showMessageDialog(this, "Employee type updated successfully", "successful", JOptionPane.INFORMATION_MESSAGE);
                        reset();
                       }
                        
                    }else{
                        JOptionPane.showMessageDialog(this, "incorrect Password", "Incorrect", JOptionPane.ERROR_MESSAGE);
                        password.setText(null);
                        password.grabFocus();
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "You are not a registerd user", "Non user", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

